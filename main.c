// S-BOX, função que cria os subBytes da criptografia
long SubByte(long byte, bool status){
  
	int i;
	int b[4];
	long temp=0;

	if (status==0){
	   b[3] = (byte & 0x000000FF); 
	   b[2] = (byte & 0x0000FF00)>>8;
	   b[1] = (byte & 0x00FF0000)>>16;
	   b[0] = (byte & 0xFF000000)>>24;
	

       temp=((SBOX[b[0]])+temp)<<8 & 0x0000FF00;
       temp=((SBOX[b[1]])+temp)<<8 & 0x00FFFF00;
       temp=((SBOX[b[2]])+temp)<<8 & 0xFFFFFF00;
       temp=((SBOX[b[3]])+temp);
       return temp;
	}
    else
    {
		  return SBOX[byte];
	}
}

//rotaciona W(word)
long RotByte(long word){
	
	unsigned long aux=0;
	unsigned long rword=0;

	aux = (unsigned) word >>24;
    rword = ((word & 0x00FFFFFF)<<8) | aux;
	return rword;
}

//Rotaciona linhas da tabela State
void ShiftRow(long t[4][4], int linha){
  
	int i;
	long aux;

	for (i=1;i<=linha;i++){
         aux        = t[0][linha];
		 t[0][linha]= t[1][linha];
		 t[1][linha]= t[2][linha];
		 t[2][linha]= t[3][linha];
		 t[3][linha]= aux; 
	}
}

