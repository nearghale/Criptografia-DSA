//função que multiplica os valores da tabela state usando polinômios 
void MixColumn(long t[4][4]){
   
	int c,r;
	long temp[4][4];
	
	for (c=0;c<=3;c++)
    {
	    for (r=0;r<=3;r++)
        {
		    temp[c][r] = FFmul(0x02,t[c][r]) ^
                 FFmul(0x03,t[c][(r+1) % 4]) ^
                 t[c][(r+2) % 4] ^
                 t[c][(r+3) % 4];
		}
	}

	for (c=0;c<=3;c++)
    {
	    for (r=0;r<=3;r++)
        {
		     t[c][r] = temp[c][r];
		}
	}
}

//função que inverte os valores obtidos depois da função MinxColumn.
