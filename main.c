void XorRoundKey(long t[4][4], long word[44], int round){
  
	int i,j;
	long temp[4][4];
	long aux[4];

	//seleciona word
    for (i=0;i<=3;i++)
    {
	    aux[i] = word[i+(round*4)];
	}
	
	// gerando matriz 4x4 (word)
	for (i=0;i<=3;i++)
    {
		for (j=0;j<=3;j++)
        {
			if (j==0) {temp[i][j]=(aux[i] & 0xFF000000)>>24;}
		    if (j==1) {temp[i][j]=(aux[i] & 0x00FF0000)>>16;}
  		    if (j==2) {temp[i][j]=(aux[i] & 0x0000FF00)>>8;}
		    if (j==3) {temp[i][j]=(aux[i] & 0x000000FF);}
		}
	}
	
	// Operação de Xor
	for (i=0;i<=3;i++)
    {
		for (j=0;j<=3;j++)
        {
            t[i][j]=temp[i][j] ^ t[i][j];
		}
	}	
}

